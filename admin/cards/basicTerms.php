						<p>The act of rummaging through the trash of an individual or business to gather information that could be useful for a cyber-criminal to gain access to a system or attain personal information to aid them in identity theft or system intrusion. One person's garbage can indeed be a cyber-criminal's treasure.</p>
					</details>
