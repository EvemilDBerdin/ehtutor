						<summary>Easter Egg</summary>
						<p>A non-malicous surprise contained in a program or ona circuit board installed by the developer. It could be as simple as a text greeting, a signature, or an image embedded on a circuit board, or comprise a more complex routie, like a video or a small program. It should be undocumented, non-malicious, reproducible to anyone with the same device or software, not be obvious, and above all - it should be entertaining.</p>
					</details>
					<details>
						<summary>Encryption</summary>
						<p>The process of scambling data or messages making it unreadable and secret. The opposite is decryption, the decoding of the message.</p>
