						<p>End-to-end encryption (E2EE) is a method of secure communication that prevents third-parties from accessing data while it's  transferred from one end system or device to another.</p>
					</details>
					<details>
						<summary>Firewall</summary>
						<p>A system using hardware, software, or both to prevent unauthorized access to a system or machine.</p>
					</details>
				</div>
				<div class="dets">
					<details>
						<summary>Hacker</summary>
						<p>While this term originally referred to a clever or expert programer who overcome a technical problem, it is now more commonly used to refer to someone who can gain unauthorized access to other computers.</p>
					</details>
					<details>
						<summary>Hacktivist</summary>
						<p>A hacktivist is someone who uses their hacking skills for political ends. And hacktivis's actions may be small, such as defacing the public website of a security agency or other government department, or large, such as stealing sensitive government information and distributing it to citizens.</p>
					</details>
